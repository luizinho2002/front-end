// Aprendendo mais sobre objetos integrados do JavaScript

// O método  Math.random() retorna um número aleatório entre 0 e 1

// O método Math.floor() retorna um número inteiro maior ou igual a um número

// O método Math.ceil retorna o menor número inteiro maior ou igual a um número

// O método isInteger() verifica se um valor é um número inteiro

console.log(Math.random() * 100);
console.log(Math.floor(Math.random() * 100));

console.log(Math.ceil(43.8));
console.log(Number.isInteger(2017));

console.log(Number.isInteger(2017));